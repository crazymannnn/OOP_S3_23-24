public class Question4 {   
    public Question4() {
    }   
    public int[] Input() {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for (int i = 0; i < n; i++ ) {
            arr[i] = sc.nextInt();
        }
        return arr;
    }
    
    public void SumOfEven(int[] arr) {
        int sum = 0;
        for (int i = 0; i < arr.length; i++) {
            if (arr[i] % 2 == 0) {
                sum = sum + arr[i];
            }
        }
        System.out.print(sum);
    }
}
